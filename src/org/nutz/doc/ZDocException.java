package org.nutz.doc;

@SuppressWarnings("serial")
public class ZDocException extends Exception {

	public ZDocException() {
		super();
	}

	public ZDocException(String message, Throwable cause) {
		super(message, cause);
	}

	public ZDocException(String message) {
		super(message);
	}

	public ZDocException(Throwable cause) {
		super(cause);
	}

}
